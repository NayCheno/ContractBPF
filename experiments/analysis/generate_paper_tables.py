def write_native_bars(root: Path, out_dir: Path) -> dict[str, str] | None:
    source = root / "experiments" / "results" / "processed" / "native_memcached_bars.csv"
    path = out_dir / "native_memcached_bars_table.tex"
    if not source.exists():
        if path.exists():
            path.unlink()
        return None

    rows = selected(read_rows(source), ["G1", "G2", "G4", "G9"])
    lines = table_header(source, root) + [
        "\\begin{table}[t]",
        "\\centering",
        "\\scriptsize",
        "\\caption{Native non-QEMU memcached conflict and recovery bars.}",
        "\\begin{tabular}{lrrrrr}",
        "\\toprule",
        "Group & P99 us & Tenant-B P99 & Queue us & Refaults & Demote state \\\\",
        "\\midrule",
    ]
    for row in rows:
        lines.append(
            " & ".join(
                [
                    escape(row["group"]),
                    as_int(row["p99_latency_us"]),
                    as_int(row["unaffected_tenant_p99_us"]),
                    as_int(row["sched_queue_delay_us"]),
                    as_int(row["refault_events"]),
                    as_int(row["demote_degrade_state"]),
                ]
            )
            + " \\\\"
        )
    lines += ["\\bottomrule", "\\end{tabular}", "\\end{table}"]
    write_lines(path, lines)
    return {"table": str(path.relative_to(root)), "source": str(source.relative_to(root)), "sha256": sha256(source)}


def main() -> int:
    root = repo_root()
    out_dir = root / "paper" / "nsdi27" / "generated"
    entries = [
        write_controlled_matrix(root, out_dir),
        write_memcached_matrix(root, out_dir),
        write_natural_bars(root, out_dir),
        write_overhead(root, out_dir),
    ]
    native_entry = write_native_bars(root, out_dir)
    if native_entry:
        entries.append(native_entry)
    manifest = {
        "generated_by": "experiments/analysis/generate_paper_tables.py",
        "tables": entries,
    }
    manifest_path = out_dir / "evidence_manifest.json"
    manifest_path.write_text(json.dumps(manifest, indent=2, sort_keys=True) + "\n", encoding="utf-8")
    for entry in entries:
        print(f"generated {entry['table']} from {entry['source']}")
    print(f"generated {manifest_path.relative_to(root)}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
