#!/usr/bin/env python3
"""Generate NSDI paper numeric tables from processed experiment CSVs."""

from __future__ import annotations

import csv
import hashlib
import json
import sys
from pathlib import Path
from typing import Iterable, List


def repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def read_rows(path: Path) -> List[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def as_int(value: str) -> str:
    try:
        return str(int(float(value)))
    except (TypeError, ValueError):
        return value


def escape(value: str) -> str:
    return (
        value.replace("\\", "\\textbackslash{}")
        .replace("&", "\\&")
        .replace("%", "\\%")
        .replace("_", "\\_")
        .replace("#", "\\#")
    )


def table_header(source: Path, root: Path) -> List[str]:
    return [
        "% Generated by experiments/analysis/generate_paper_tables.py.",
        f"% Source: {source.relative_to(root)}",
        f"% Source-SHA256: {sha256(source)}",
        "",
    ]


def write_lines(path: Path, lines: Iterable[str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def selected(rows: List[dict[str, str]], groups: Iterable[str]) -> List[dict[str, str]]:
    by_group = {row["group"]: row for row in rows}
    return [by_group[group] for group in groups if group in by_group]


def write_controlled_matrix(root: Path, out_dir: Path) -> dict[str, str]:
    source = root / "experiments" / "results" / "processed" / "matrix_summary.csv"
    rows = selected(read_rows(source), [f"G{i}" for i in range(1, 10)])
    path = out_dir / "controlled_qemu_matrix_table.tex"
    lines = table_header(source, root) + [
        "\\begin{table}[t]",
        "\\centering",
        "\\scriptsize",
        "\\caption{Controlled QEMU matrix summary. Latencies are guest synthetic-service samples.}",
        "\\begin{tabular}{lrrrr}",
        "\\toprule",
        "Group & P99 us & Queue delay us & Demoted & Demote state \\\\",
        "\\midrule",
    ]
    for row in rows:
        lines.append(
            " & ".join(
                [
                    escape(row["group"]),
                    as_int(row["p99_latency_us"]),
                    as_int(row["sched_queue_delay_us"]),
                    as_int(row["pages_demoted_per_epoch"]),
                    as_int(row["demote_degrade_state"]),
                ]
            )
            + " \\\\"
        )
    lines += ["\\bottomrule", "\\end{tabular}", "\\end{table}"]
    write_lines(path, lines)
    return {"table": str(path.relative_to(root)), "source": str(source.relative_to(root)), "sha256": sha256(source)}


